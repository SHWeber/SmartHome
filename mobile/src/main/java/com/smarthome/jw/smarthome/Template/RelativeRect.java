public class RelativeRect {

    public Double Top;
    public Double Bottom;
    public Double Left;
    public Double Right;

    public RelativeRect(Double top, Double bottom, Double left, Double right) {

        Top = top;
        Bottom = bottom;
        Left = left;
        Right = right;
    }
}
